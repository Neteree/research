# Declares the float pi and initialises it with the value 3.1415
pi = 3.1415
# Declares the float e and initialises it with the value 2.718
e = 2.718
# Declares the float one_over_eight and initialises it with the quotient of 1/8
one_over_eight = 1/8

# Outputs the value of pi
print(pi)
# Outputs the value of e
print(e)
# Outputs the value of one_over_eight
print(one_over_eight)
# Outputs the product of 2 and pi
print(2 * pi)
# Outputs the product of 4 and e
print(4 * e)
# Outputs the product of 8 and one_over_eight
print(8 * one_over_eight)
